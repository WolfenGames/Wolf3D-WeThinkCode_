#include "../includes/wolf3d.h"

int		draw_mini(t_wolf *w)
{
	int		x;
	int		y;
	int		off;

	off = 30;
	x = 0;
	while (x < w->h)
	{
		y = 0;
		while (y < w->w)
		{
			put_pixel(x, y, 0x000000, w);
			y++;
		}
		x++;
	}	
}
