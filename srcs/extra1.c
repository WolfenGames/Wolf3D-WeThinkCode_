#include "../includes/wolf3d.h"

void	new_image(t_wolf *w)
{
	w->img = mlx_new_image(w->mlx, w->wi.c_w, w->wi.c_h);
	w->dat = mlx_get_data_addr(w->img, &w->bpp, &w->sl, &w->endn);
	w->bpp /= 8;
}
